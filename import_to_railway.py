def import_played_for(driver: Driver, export_dir: Path) -> int:
    """MERGE PLAYED_FOR relationships from rels_PLAYED_FOR.json.

    MERGE key: (Player.id, Team.school, year).

    Args:
        driver: Open Neo4j driver.
        export_dir: Directory containing the migration dump.

    Returns:
        Number of records submitted.
    """
    rows = _load_json(export_dir, "rels_PLAYED_FOR.json")
    _run_batched(
        driver,
        """
        UNWIND $rows AS row
        MATCH (p:Player {id: row.player_id})
        MATCH (t:Team   {school: row.team_school})
        MERGE (p)-[r:PLAYED_FOR {year: row.year}]->(t)
        SET r.jersey = row.jersey
        """,
        rows,
        "PLAYED_FOR rels",
    )
    return len(rows)


def import_coached_at_cfbd(driver: Driver, export_dir: Path) -> int:
    """MERGE COACHED_AT (source=None/CFBD) from rels_COACHED_AT_cfbd.json.

    MERGE key: (Coach.first_name, Coach.last_name, Team.school, title,
                start_year) — matches original load_coaches() logic.

    Args:
        driver: Open Neo4j driver.
        export_dir: Directory containing the migration dump.

    Returns:
        Number of records submitted.
    """
    rows = _load_json(export_dir, "rels_COACHED_AT_cfbd.json")
    _run_batched(
        driver,
        """
        UNWIND $rows AS row
        MATCH (c:Coach {first_name: row.first_name, last_name: row.last_name})
        MATCH (t:Team  {school: row.team_school})
        MERGE (c)-[r:COACHED_AT {title: row.title, start_year: row.start_year}]->(t)
        SET r.end_year = row.end_year
        """,
        rows,
        "COACHED_AT (cfbd) rels",
    )
    return len(rows)


def import_coached_at_mcillece(driver: Driver, export_dir: Path) -> int:
    """MERGE COACHED_AT (source='mcillece') from rels_COACHED_AT_mcillece.json.

    MERGE key: (coach_code, year, team_code) — matches load_staff() logic.

    Args:
        driver: Open Neo4j driver.
        export_dir: Directory containing the migration dump.

    Returns:
        Number of records submitted.
    """
    rows = _load_json(export_dir, "rels_COACHED_AT_mcillece.json")
    _run_batched(
        driver,
        """
        UNWIND $rows AS row
        MATCH (c:Coach {coach_code: row.coach_code})
        MATCH (t:Team  {school: row.team_school})
        MERGE (c)-[r:COACHED_AT {
            coach_code: row.coach_code,
            year:       row.year,
            team_code:  row.team_code
        }]->(t)
        SET r.roles  = row.roles,
            r.source = 'mcillece'
        """,
        rows,
        "COACHED_AT (mcillece) rels",
    )
    return len(rows)


def import_coached_at_mcillece_roles(driver: Driver, export_dir: Path) -> int:
    """MERGE COACHED_AT (source='mcillece_roles') from rels_COACHED_AT_mcillece_roles.json.

    MERGE key: (coach_code, year, team_code, role_abbr) — matches
    load_coached_at_roles() logic.

    Args:
        driver: Open Neo4j driver.
        export_dir: Directory containing the migration dump.

    Returns:
        Number of records submitted.
    """
    rows = _load_json(export_dir, "rels_COACHED_AT_mcillece_roles.json")

    # Flatten rel_props dict into the row so UNWIND can access individual fields
    flat = [
        {
            "coach_code": r["coach_code"],
            "team_school": r["team_school"],
            **r["rel_props"],
        }
        for r in rows
    ]

    _run_batched(
        driver,
        """
        UNWIND $rows AS row
        MATCH (c:Coach {coach_code: row.coach_code})
        MATCH (t:Team  {school: row.team_school})
        MERGE (c)-[r:COACHED_AT {
            coach_code: row.coach_code,
            year:       row.year,
            team_code:  row.team_code,
            role_abbr:  row.role_abbr
        }]->(t)
        SET r.role           = row.role,
            r.role_tier      = row.role_tier,
            r.is_coordinator = row.is_coordinator,
            r.coach_name     = row.coach_name,
            r.source         = 'mcillece_roles'
        """,
        flat,
        "COACHED_AT (mcillece_roles) rels",
    )
    return len(rows)


def import_played(driver: Driver, export_dir: Path) -> int:
    """MERGE PLAYED relationships from rels_PLAYED.json.

    MERGE key: game_id on the relationship.

    Args:
        driver: Open Neo4j driver.
        export_dir: Directory containing the migration dump.

    Returns:
        Number of records submitted.
    """
    rows = _load_json(export_dir, "rels_PLAYED.json")

    flat = [
        {
            "home_school": r["home_school"],
            "away_school": r["away_school"],
            **r["rel_props"],
        }
        for r in rows
    ]

    _run_batched(
        driver,
        """
        UNWIND $rows AS row
        MATCH (home:Team {school: row.home_school})
        MATCH (away:Team {school: row.away_school})
        MERGE (home)-[r:PLAYED {game_id: row.game_id}]->(away)
        SET r.home_score = row.home_score,
            r.away_score = row.away_score,
            r.season     = row.season,
            r.week       = row.week
        """,
        flat,
        "PLAYED rels",
    )
    return len(rows)


def import_in_conference(driver: Driver, export_dir: Path) -> int:
    """MERGE IN_CONFERENCE relationships from rels_IN_CONFERENCE.json.

    Args:
        driver: Open Neo4j driver.
        export_dir: Directory containing the migration dump.

    Returns:
        Number of records submitted.
    """
    rows = _load_json(export_dir, "rels_IN_CONFERENCE.json")
    _run_batched(
        driver,
        """
        UNWIND $rows AS row
        MATCH (t:Team       {school: row.team_school})
        MATCH (c:Conference {name:   row.conference_name})
        MERGE (t)-[:IN_CONFERENCE]->(c)
        """,
        rows,
        "IN_CONFERENCE rels",
    )
    return len(rows)


def import_mentored(driver: Driver, export_dir: Path) -> int:
    """MERGE MENTORED relationships from rels_MENTORED.json.

    Coaches may be identified by first/last name (CFBD) or coach_code
    (McIllece).  Pairs are split by available identity and loaded with
    the appropriate MATCH key.

    Args:
        driver: Open Neo4j driver.
        export_dir: Directory containing the migration dump.

    Returns:
        Number of records submitted.
    """
    rows = _load_json(export_dir, "rels_MENTORED.json")

    # Coaches with coach_code on both ends → match by code
    by_code = [
        r for r in rows
        if r.get("mentor_code") and r.get("mentee_code")
    ]
    # All others → match by first/last name
    by_name = [
        r for r in rows
        if not (r.get("mentor_code") and r.get("mentee_code"))
    ]

    if by_name:
        _run_batched(
            driver,
            """
            UNWIND $rows AS row
            MATCH (mentor:Coach {first_name: row.mentor_first, last_name: row.mentor_last})
            MATCH (mentee:Coach {first_name: row.mentee_first, last_name: row.mentee_last})
            MERGE (mentor)-[:MENTORED]->(mentee)
            """,
            by_name,
            "MENTORED rels (name-keyed)",
        )

    if by_code:
        _run_batched(
            driver,
            """
            UNWIND $rows AS row
            MATCH (mentor:Coach {coach_code: row.mentor_code})
            MATCH (mentee:Coach {coach_code: row.mentee_code})
            MERGE (mentor)-[:MENTORED]->(mentee)
            """,
            by_code,
            "MENTORED rels (code-keyed)",
        )

    return len(rows)


