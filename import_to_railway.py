def import_teams(driver: Driver, export_dir: Path) -> int:
    """MERGE Team nodes from nodes_Team.json.

    Args:
        driver: Open Neo4j driver.
        export_dir: Directory containing the migration dump.

    Returns:
        Number of records submitted.
    """
    rows = _load_json(export_dir, "nodes_Team.json")
    _run_batched(
        driver,
        """
        UNWIND $rows AS row
        MERGE (t:Team {id: row.id})
        SET t.school       = row.school,
            t.conference   = row.conference,
            t.abbreviation = row.abbreviation
        """,
        rows,
        "Team nodes",
    )
    return len(rows)


def import_conferences(driver: Driver, export_dir: Path) -> int:
    """MERGE Conference nodes from nodes_Conference.json.

    Args:
        driver: Open Neo4j driver.
        export_dir: Directory containing the migration dump.

    Returns:
        Number of records submitted.
    """
    rows = _load_json(export_dir, "nodes_Conference.json")
    _run_batched(
        driver,
        "UNWIND $rows AS row MERGE (c:Conference {name: row.name})",
        rows,
        "Conference nodes",
    )
    return len(rows)


def import_coaches(driver: Driver, export_dir: Path) -> int:
    """MERGE Coach nodes from nodes_Coach.json.

    Handles both CFBD coaches (first_name/last_name) and McIllece coaches
    (coach_code).  Each coach type is submitted separately so the correct
    MERGE key is used.

    Args:
        driver: Open Neo4j driver.
        export_dir: Directory containing the migration dump.

    Returns:
        Number of records submitted.
    """
    rows = _load_json(export_dir, "nodes_Coach.json")

    # CFBD coaches are keyed by (first_name, last_name)
    cfbd = [r for r in rows if r.get("first_name") and not r.get("coach_code")]
    # McIllece coaches are keyed by coach_code
    mcillece = [r for r in rows if r.get("coach_code")]

    if cfbd:
        _run_batched(
            driver,
            """
            UNWIND $rows AS row
            MERGE (c:Coach {first_name: row.first_name, last_name: row.last_name})
            """,
            cfbd,
            "Coach nodes (cfbd)",
        )

    if mcillece:
        _run_batched(
            driver,
            """
            UNWIND $rows AS row
            MERGE (c:Coach {coach_code: row.coach_code})
            SET c.name       = row.name,
                c.coach_code = row.coach_code
            """,
            mcillece,
            "Coach nodes (mcillece)",
        )

    return len(rows)


def import_players(driver: Driver, export_dir: Path) -> int:
    """MERGE Player nodes from nodes_Player.json.

    Args:
        driver: Open Neo4j driver.
        export_dir: Directory containing the migration dump.

    Returns:
        Number of records submitted.
    """
    rows = _load_json(export_dir, "nodes_Player.json")
    _run_batched(
        driver,
        """
        UNWIND $rows AS row
        MERGE (p:Player {id: row.id})
        SET p.name     = row.name,
            p.position = row.position,
            p.hometown = row.hometown
        """,
        rows,
        "Player nodes",
    )
    return len(rows)


