def import_all(driver: Driver, export_dir: Path) -> dict[str, int]:
    """Import all nodes and relationships from the export directory.

    Load order mirrors the original pipeline to satisfy FK-like MATCH
    requirements (Teams must exist before Conferences links them, etc.).

    Args:
        driver: Open Neo4j driver pointing at Railway.
        export_dir: Directory produced by export_auradb.py.

    Returns:
        ``{label: count}`` for every import step.
    """
    create_constraints(driver)

    counts: dict[str, int] = {}

    # Nodes first — order matters (Teams → Conferences → Coaches → Players)
    counts["nodes_Team"]       = import_teams(driver, export_dir)
    counts["nodes_Conference"] = import_conferences(driver, export_dir)
    counts["nodes_Coach"]      = import_coaches(driver, export_dir)
    counts["nodes_Player"]     = import_players(driver, export_dir)

    # Relationships — after all nodes exist
    counts["rels_PLAYED_FOR"]                = import_played_for(driver, export_dir)
    counts["rels_COACHED_AT_cfbd"]           = import_coached_at_cfbd(driver, export_dir)
    counts["rels_COACHED_AT_mcillece"]       = import_coached_at_mcillece(driver, export_dir)
    counts["rels_COACHED_AT_mcillece_roles"] = import_coached_at_mcillece_roles(driver, export_dir)
    counts["rels_PLAYED"]                    = import_played(driver, export_dir)
    counts["rels_IN_CONFERENCE"]             = import_in_conference(driver, export_dir)
    counts["rels_MENTORED"]                  = import_mentored(driver, export_dir)

    return counts


