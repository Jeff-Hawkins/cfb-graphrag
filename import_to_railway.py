def _latest_export_dir() -> Path:
    """Return the most recently created export directory under data/migrations/.

    Returns:
        Path to the export directory.

    Raises:
        FileNotFoundError: If no export directories exist.
    """
    base = Path("data/migrations")
    dirs = sorted(base.glob("auradb_export_*"), reverse=True)
    if not dirs:
        raise FileNotFoundError(
            f"No auradb_export_* directories found under {base}. "
            "Run export_auradb.py first."
        )
    return dirs[0]


def main(*, export_dir: Path | None = None, dry_run: bool = False) -> None:
    """Run the full Railway import.

    Args:
        export_dir: Override the export directory (defaults to latest).
        dry_run: If True, load files and print counts without writing.
    """
    resolved_dir = export_dir or _latest_export_dir()
    logger.info("Import source: %s", resolved_dir)

    if dry_run:
        logger.info("[dry-run] Reading export files — no Railway writes")
        for filename in sorted(resolved_dir.glob("*.json")):
            with open(filename, encoding="utf-8") as fh:
                data = json.load(fh)
            print(f"  {filename.name:<50} {len(data):>10,}")
        return

    uri = os.environ.get("RAILWAY_NEO4J_URI", "")
    user = os.environ.get("RAILWAY_NEO4J_USER", "")
    password = os.environ.get("RAILWAY_NEO4J_PASSWORD", "")
    if not all([uri, user, password]):
        raise EnvironmentError(
            "RAILWAY_NEO4J_URI, RAILWAY_NEO4J_USER, and RAILWAY_NEO4J_PASSWORD "
            "must be set in .env"
        )

    from loader.neo4j_loader import get_driver

    driver = get_driver(uri, user, password)
    try:
        counts = import_all(driver, resolved_dir)
    finally:
        driver.close()

    total_nodes = sum(v for k, v in counts.items() if k.startswith("nodes_"))
    total_rels = sum(v for k, v in counts.items() if k.startswith("rels_"))

    print("\n=== Railway Import Summary ===")
    for key in sorted(counts):
        print(f"  {key:<45} {counts[key]:>10,}")
    print(f"\n  Total nodes         {total_nodes:>10,}")
    print(f"  Total relationships {total_rels:>10,}")
    print("\nImport complete. Run verify_railway.py to confirm counts.")


if __name__ == "__main__":
    args = sys.argv[1:]
    dir_arg = next((a for a in args if a.startswith("--export-dir=")), None)
    resolved = Path(dir_arg.split("=", 1)[1]) if dir_arg else None
    main(export_dir=resolved, dry_run="--dry-run" in args)
